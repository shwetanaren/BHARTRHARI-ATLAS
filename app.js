function parseLibraryNumberValue(value, workId = state.libraryWorkId) {
  const sample = libraryCollections[workId]?.verses?.[0]?.number;
  return typeof sample === "number" ? Number(value) : value;
}

function workEntryUnit(workId, count) {
  if (state.language === "te") {
    return isVakyaWork(workId) ? "కారికాలు" : "పద్యాలు";
  }

  return isVakyaWork(workId) ? (count === 1 ? "kārikā" : "kārikās") : "verses";
}

function formatVerseCitation(verse) {
  if (state.language === "te") {
    return `${workLabel(verse.workId)} · ${formatVerseReference(verse.workId, verse.number)} · ${verse.gretilId}`;
  }

  return `${workLabel(verse.workId)} · ${formatVerseReference(verse.workId, verse.number)} · ${verse.gretilId}`;
}

function renderVerseLines(lines, className) {
  return lines
    .map((line) => `<span class="${className}-line">${line}</span>`)
    .join("<br />");
}

function renderCanonicalLinks(workId) {
  const sources = isVakyaWork(workId)
    ? Object.values(vakyaCanonicalSources)
    : Object.values(canonicalSources);

  return sources
    .map(
      (source) => `
        <a href="${source.url}" target="_blank" rel="noreferrer">
          ${pick(source.label)}
        </a>
      `
    )
    .join("");
}

function normalizeText(value = "") {
  return value
    .normalize("NFD")
    .replace(/\p{M}/gu, "")
    .toLowerCase();
}

function getActiveGuide() {
  return getActiveLibraryGuides().find((guide) => guide.id === state.libraryGuideId) || null;
}

function getLibraryRendering(verse, featuredContext) {
  const renderings = getActiveLibraryCollection()?.renderings || {};
  const rendering = renderings[verse.gretilId];

  if (rendering) {
    if (typeof rendering === "string") {
      return state.language === "te" ? "" : rendering;
    }

    return rendering[state.language] || rendering.en || "";
  }

  if (featuredContext) {
    return featuredContext.verse.rendering[state.language] || featuredContext.verse.rendering.en;
  }

  return "";
}

function getFilteredLibrary() {
  const activeLibrary = getActiveLibraryCollection();
  const query = normalizeText(state.libraryQuery);
  const activeGuide = getActiveGuide();
  const guideNumbers = activeGuide ? new Set(activeGuide.verseNumbers) : null;

  return (activeLibrary?.verses || []).filter((verse) => {
    if (guideNumbers && !guideNumbers.has(verse.number)) {
      return false;
    }

    if (!query) {
      return true;
    }

    return normalizeText(
      `${verse.number} ${verse.gretilId} ${verse.opening} ${verse.lines.join(" ")}`
    ).includes(query);
  });
}

function getFilteredLibraryForGuide(guideId) {
  const activeLibrary = getActiveLibraryCollection();
  const query = normalizeText(state.libraryQuery);
  const guide =
    guideId === "all" ? null : getActiveLibraryGuides().find((entry) => entry.id === guideId);
  const guideNumbers = guide ? new Set(guide.verseNumbers) : null;

  return (activeLibrary?.verses || []).filter((verse) => {
    if (guideNumbers && !guideNumbers.has(verse.number)) {
      return false;
    }

    if (!query) {
      return true;
    }

    return normalizeText(
      `${verse.number} ${verse.gretilId} ${verse.opening} ${verse.lines.join(" ")}`
    ).includes(query);
  });
}

function syncLibrarySelection() {
  const filtered = getFilteredLibrary();

  if (!filtered.length) {
    state.libraryVerseNumber = null;
    return filtered;
  }

  if (!filtered.some((verse) => verse.number === state.libraryVerseNumber)) {
    state.libraryVerseNumber = filtered[0].number;
  }

  return filtered;
}

function getLibraryVerse() {
  return getFilteredLibrary().find((verse) => verse.number === state.libraryVerseNumber) || null;
}

function getGuideIdForVerse(workId, verseNumber) {
  const guides = libraryCollections[workId]?.guides || [];
  return guides.find((guide) => guide.verseNumbers.includes(verseNumber))?.id || "all";
}

function getCrossWorkRecommendations(featuredContext) {
  if (!featuredContext) {
    return [];
  }

  const relatedContexts = [featuredContext, ...featuredContext.related.map((relatedId) => getContext(relatedId))];

  return relatedContexts
    .map((context) => {
      const verse = context.verse;
      return {
        contextId: context.id,
        label: pick(context.label),
        strap: pick(context.strap),
        workId: verse.workId,
        verseNumber: verse.number,
        gretilId: verse.gretilId,
        guideId: getGuideIdForVerse(verse.workId, verse.number)
      };
    })
    .filter((item, index, list) => {
      if (item.gretilId === featuredContext.verse.gretilId) {
        return false;
      }

      return list.findIndex((candidate) => candidate.gretilId === item.gretilId) === index;
    })
    .slice(0, 4);
}

function renderStaticCopy() {
  document.title = t("documentTitle");
  document.documentElement.lang = state.language;
  document.body.classList.toggle("is-telugu", state.language === "te");

  staticTextNodes.forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });

  staticPlaceholderNodes.forEach((node) => {
    node.setAttribute("placeholder", t(node.dataset.i18nPlaceholder));
  });

  languageButtons.forEach((button) => {
    const active = button.dataset.language === state.language;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });

  renderWorksRows();
}

