function renderLibraryGuides() {
  if (!libraryWorkSwitch) {
    return;
  }

  libraryWorkSwitch.innerHTML = libraryWorkOrder
    .map((workId) => {
      const activeClass = workId === state.libraryWorkId ? "is-active" : "";
      return `
        <button
          type="button"
          class="library-work-button ${activeClass}"
          data-library-work="${workId}"
          aria-pressed="${workId === state.libraryWorkId}"
        >
          ${workLabel(workId)}
        </button>
      `;
    })
    .join("");

  Array.from(libraryWorkSwitch.querySelectorAll("[data-library-work]")).forEach((button) => {
    button.addEventListener("click", () => {
      const nextWorkId = button.dataset.libraryWork;
      const nextCollection = libraryCollections[nextWorkId];
      state.libraryWorkId = nextWorkId;
      state.libraryGuideId = nextCollection?.guides[0]?.id || "all";
      state.libraryVerseNumber =
        nextCollection?.guides[0]?.verseNumbers?.[0] ?? nextCollection?.verses[0]?.number ?? null;
      state.libraryQuery = "";
      if (librarySearch) {
        librarySearch.value = "";
      }
      renderAll();
    });
  });
}

function renderLibrarySummary(filteredVerses) {
  if (!librarySummary) {
    return;
  }

  const activeLibrary = getActiveLibraryCollection();
  const total = activeLibrary?.meta?.verseCount || activeLibrary?.verses?.length || 0;
  const showing = filteredVerses.length;
  const activeGuide = getActiveGuide();
  const isAll = state.libraryGuideId === "all";
  const countLine =
    state.language === "te"
      ? `ఇప్పుడు ${showing} / ${total} ${workEntryUnit(state.libraryWorkId, total)} కనిపిస్తున్నాయి.`
      : `Showing ${showing} of ${total} ${workEntryUnit(state.libraryWorkId, total)} right now.`;
  const leadLine = isAll
    ? t("librarySummaryAllVerses")
    : t("librarySummarySituation");
  const workLine =
    state.language === "te"
      ? `${workLabel(state.libraryWorkId)} · ${t("libraryEditionName")}`
      : `${workLabel(state.libraryWorkId)} · ${t("libraryEditionName")}`;
  const guideLine = activeGuide ? `<p>${pick(activeGuide.prompt)}</p>` : "";
  const activeLine =
    !isAll && activeGuide
      ? `<p><strong>${pick(activeGuide.label)}</strong></p>`
      : "";

  librarySummary.innerHTML = `
    <p>${workLine}</p>
    <p>${leadLine}</p>
    ${activeLine}
    ${guideLine}
    <p>${countLine}</p>
  `;
}

function renderLibraryList() {
  if (!libraryList) {
    return;
  }

  const filteredVerses = syncLibrarySelection();
  renderLibrarySummary(filteredVerses);

  const accordionSections = [
    ...getActiveLibraryGuides().map((guide) => ({
      id: guide.id,
      label: pick(guide.label),
      prompt: pick(guide.prompt)
    })),
    { id: "all", label: t("libraryGuideAll"), prompt: t("librarySummaryAll") }
  ];

  libraryList.innerHTML = accordionSections
    .map((section) => {
      const sectionVerses = getFilteredLibraryForGuide(section.id);
      const isActive = state.libraryGuideId === section.id;
      const countLabel =
        state.language === "te"
          ? `${sectionVerses.length} ${workEntryUnit(state.libraryWorkId, sectionVerses.length)}`
          : `${sectionVerses.length} ${workEntryUnit(state.libraryWorkId, sectionVerses.length)}`;

      return `
        <section class="library-section ${isActive ? "is-open" : ""}">
          <button
            type="button"
            class="library-section-toggle ${isActive ? "is-active" : ""}"
            data-guide-id="${section.id}"
            aria-expanded="${isActive}"
            aria-controls="library-panel-${section.id}"
          >
            <span class="guide-name">${section.label}</span>
            <span class="guide-prompt">${section.prompt}</span>
            <span class="library-section-count">${countLabel}</span>
          </button>
          <div
            class="library-section-panel"
            id="library-panel-${section.id}"
            ${isActive ? "" : "hidden"}
          >
            ${
              sectionVerses.length
                ? sectionVerses
                    .map((verse) => {
                      const activeClass =
                        verse.number === state.libraryVerseNumber ? "is-active" : "";
                      return `
                        <button
                          type="button"
                          class="library-button ${activeClass}"
                          data-library-number="${verse.number}"
                          data-guide-id="${section.id}"
                          aria-selected="${verse.number === state.libraryVerseNumber}"
                          aria-current="${verse.number === state.libraryVerseNumber ? "true" : "false"}"
                          aria-controls="library-detail"
                        >
                          <span class="library-number">${formatLibraryNumber(verse)}</span>
                          <span class="library-opening">${verse.opening}</span>
                        </button>
                      `;
                    })
                    .join("")
                : `<div class="library-empty-list"><p>${t("libraryNoResultsTitle")}</p></div>`
            }
          </div>
        </section>
      `;
    })
    .join("");

  Array.from(libraryList.querySelectorAll("[data-guide-id]")).forEach((button) => {
    if (button.matches(".library-section-toggle")) {
      button.addEventListener("click", () => {
        state.libraryGuideId = button.dataset.guideId;
        const sectionVerses = getFilteredLibraryForGuide(state.libraryGuideId);
        if (sectionVerses.length && !sectionVerses.some((verse) => verse.number === state.libraryVerseNumber)) {
          state.libraryVerseNumber = sectionVerses[0].number;
        }
        renderAll();
      });
    }
  });

  Array.from(libraryList.querySelectorAll("[data-library-number]")).forEach((button) => {
    button.addEventListener("click", () => {
      state.libraryGuideId = button.dataset.guideId || state.libraryGuideId;
      state.libraryVerseNumber = parseLibraryNumberValue(button.dataset.libraryNumber);
      renderAll();
    });
  });
}

